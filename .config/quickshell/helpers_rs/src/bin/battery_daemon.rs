use futures_util::stream::StreamExt;
use serde::Serialize;
use std::error::Error;
use std::io::Write;
use zbus::{Connection, proxy};

// D-Bus proxy for UPower's DisplayDevice
#[proxy(
    interface = "org.freedesktop.UPower.Device",
    default_service = "org.freedesktop.UPower",
    default_path = "/org/freedesktop/UPower/devices/DisplayDevice"
)]
trait UPowerDevice {
    #[zbus(property)]
    fn percentage(&self) -> zbus::Result<f64>;

    #[zbus(property)]
    fn state(&self) -> zbus::Result<u32>;
}

#[derive(Serialize)]
struct BatteryEvent {
    capacity: i32,
    status: String,
}

#[tokio::main]
async fn main() -> Result<(), Box<dyn Error>> {
    // Connect to the system bus
    let connection = Connection::system().await?;
    let proxy = UPowerDeviceProxy::new(&connection).await?;

    // Print the initial state immediately on startup
    let initial_pct = proxy.percentage().await?;
    let initial_state = proxy.state().await?;
    broadcast_state(initial_pct, initial_state);

    // Subscribe to specific property streams generated by zbus
    let mut state_changes = proxy.receive_state_changed().await;
    let mut pct_changes = proxy.receive_percentage_changed().await;

    // Block asynchronously and wake up when either property emits a D-Bus signal
    loop {
        tokio::select! {
            Some(_) = state_changes.next() => {}
            Some(_) = pct_changes.next() => {}
        }

        // Fetch the fresh values
        if let (Ok(pct), Ok(state)) = (proxy.percentage().await, proxy.state().await) {
            broadcast_state(pct, state);
        }
    }
}

fn broadcast_state(percentage: f64, state: u32) {
    // UPower state enums: 1=Charging, 2=Discharging, 3=Empty, 4=Fully charged
    let status_str = match state {
        1 => "Charging",
        2 => "Discharging",
        4 => "Full",
        _ => "Unknown",
    };

    let event = BatteryEvent {
        capacity: percentage.round() as i32,
        status: status_str.to_string(),
    };

    if let Ok(json) = serde_json::to_string(&event) {
        println!("{}", json);
        // Force flush stdout to ensure Quickshell receives the line immediately
        let _ = std::io::stdout().flush();
    }
}
